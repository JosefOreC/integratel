import numpy as np

# Crear diccionario base
data = {
    "Indicador": [
        "ARPU promedio (S/.)",
        "Ingresos totales muestra (S/.)",
        "Eventos de churn",
        "MTTR promedio (min)",
        "Utilización de red (%)",
        "Satisfacción cliente (1-10)"
    ]
}

# Años a analizar
years_to_analyze = [2020, 2021, 2022, 2023, 2024]
churn_anual = (
    fact_churn[
        fact_churn["año"].between(2020, 2024)
    ]
    .groupby("año")
    .size()
    .reset_index(name="bajas")
)

# Llenar columnas por año
for year in years_to_analyze:

    # Obtener métricas
    arpu = fact_churn[fact_churn["año"] == year]["arpu_promedio_sol"].mean()

    ingresos = fact_fact[fact_fact["año"] == year]["total_sol"].sum()

    bajas_df = churn_anual[churn_anual["año"] == year]

    bajas = bajas_df["bajas"].iloc[0] if not bajas_df.empty else np.nan

    mttr = fact_av[fact_av["año"] == year]["mttr_minutos"].mean()

    utilizacion = fact_red.loc[
    fact_red["año"] == year,
    "utilizacion_pct"
    ].mean()

    satisfaccion = fact_av[fact_av["año"] == year]["satisfaccion_1_10"].mean()

    # Guardar
    data[str(year)] = [
        arpu,
        ingresos,
        bajas,
        mttr,
        utilizacion,
        satisfaccion
    ]

# Crear DataFrame
tabla = pd.DataFrame(data)

