from xgboost import XGBClassifier
from imblearn.over_sampling import SMOTE

from sklearn.model_selection import train_test_split
from sklearn.preprocessing import LabelEncoder
from sklearn.metrics import (
    classification_report,
    roc_auc_score,
    roc_curve
)

import pandas as pd
import numpy as np

# ─────────────────────────────────────────────────────────────
# DATASET ANALÍTICO
# ─────────────────────────────────────────────────────────────

av_cl = (
    fact_av
    .groupby("id_cliente")
    .agg(
        num_reclamos=("genero_reclamo", "sum"),
        mttr_prom=("mttr_minutos", "mean"),
        sat_media=("satisfaccion_1_10", "mean"),
        total_averias=("id_cliente", "count")
    )
    .reset_index()
)

ff_cl = (
    fact_fact
    .groupby("id_cliente")
    .agg(
        arpu=("total_sol", "mean"),

        pct_venc=(
            "dias_atraso",
            lambda x: (x > 0).mean() * 100
        ),

        deuda_promedio=("dias_atraso", "mean"),
        max_dias_atraso=("dias_atraso", "max")
    )
    .reset_index()
)

df_model = (
    dim_cliente[
        [
            "id_cliente",
            "segmento",
            "departamento",
            "antiguedad_meses"
        ]
    ]

    .merge(
        av_cl,
        on="id_cliente",
        how="left"
    )

    .merge(
        ff_cl,
        on="id_cliente",
        how="left"
    )
)

# ─────────────────────────────────────────────────────────────
# VARIABLE OBJETIVO
# ─────────────────────────────────────────────────────────────

df_model["churn"] = (
    dim_cliente
    .set_index("id_cliente")["estado_actual"]
    .map({
        "Baja": 1,
        "Activo": 0,
        "Suspendido": 0
    })
    .values
)

# ─────────────────────────────────────────────────────────────
# LIMPIEZA
# ─────────────────────────────────────────────────────────────

df_model = df_model.dropna(subset=["churn"])

df_model = df_model.fillna(0)

df_model["mttr_prom"] = (
    df_model["mttr_prom"]
    .clip(0, 500)
)

df_model["pct_venc"] = (
    df_model["pct_venc"]
    .clip(0, 100)
)

# ─────────────────────────────────────────────────────────────
# ENCODING
# ─────────────────────────────────────────────────────────────

le_seg = LabelEncoder()

le_dep = LabelEncoder()

df_model["seg_enc"] = (
    le_seg.fit_transform(df_model["segmento"])
)

df_model["dep_enc"] = (
    le_dep.fit_transform(df_model["departamento"])
)

# ─────────────────────────────────────────────────────────────
# VARIABLES DEL MODELO
# ─────────────────────────────────────────────────────────────

features = [
    "antiguedad_meses",
    "num_reclamos",
    "mttr_prom",
    "sat_media",
    "total_averias",
    "arpu",
    "pct_venc",
    "deuda_promedio",
    "max_dias_atraso",
    "seg_enc",
    "dep_enc"
]

X = df_model[features]

y = df_model["churn"]

# ─────────────────────────────────────────────────────────────
# INFORMACIÓN GENERAL
# ─────────────────────────────────────────────────────────────

print("="*70)
print("DATASET DEL MODELO")
print("="*70)

print("Total clientes:", len(df_model))

print(
    "Tasa de churn:",
    round(y.mean() * 100, 2),
    "%"
)

# ─────────────────────────────────────────────────────────────
# TRAIN / TEST
# ─────────────────────────────────────────────────────────────

X_train, X_test, y_train, y_test = train_test_split(

    X,
    y,

    test_size=0.30,

    random_state=42,

    stratify=y
)

# ─────────────────────────────────────────────────────────────
# SMOTE SOLO EN TRAIN
# ─────────────────────────────────────────────────────────────

smote = SMOTE(
    random_state=42
)

X_train_res, y_train_res = smote.fit_resample(
    X_train,
    y_train
)

# ─────────────────────────────────────────────────────────────
# MODELO XGBOOST
# ─────────────────────────────────────────────────────────────

clf = XGBClassifier(
    objective="binary:logistic",
    eval_metric="auc",
    n_estimators=120,
    max_depth=3,
    learning_rate=0.03,
    subsample=0.70,
    colsample_bytree=0.70,
    reg_alpha=2,
    reg_lambda=4,
    min_child_weight=8,
    gamma=1,
    random_state=42
)

# ─────────────────────────────────────────────────────────────
# ENTRENAMIENTO
# ─────────────────────────────────────────────────────────────

clf.fit(
    X_train_res,
    y_train_res
)

# ─────────────────────────────────────────────────────────────
# PREDICCIÓN
# ─────────────────────────────────────────────────────────────

y_prob = clf.predict_proba(X_test)[:,1]

threshold = 0.60

y_pred = (
    y_prob >= threshold
).astype(int)

# ─────────────────────────────────────────────────────────────
# MÉTRICAS
# ─────────────────────────────────────────────────────────────

auc = roc_auc_score(
    y_test,
    y_prob
)

print("\n" + "="*70)
print("RESULTADOS DEL MODELO")
print("="*70)

print(
    "AUC:",
    round(auc, 4)
)

print(
    "\nThreshold:",
    threshold
)

print("\nREPORTE DE CLASIFICACIÓN\n")

print(
    classification_report(
        y_test,
        y_pred
    )
)

