# @title 3.1 Modelo Predictivo de Churn con XGBoost
# Este bloque construye y evalúa un modelo de clasificación XGBoost para predecir el churn.
# Central en las fases de Modelado y Evaluación de CRISP-DM, este código es la base para Integratel Perú
